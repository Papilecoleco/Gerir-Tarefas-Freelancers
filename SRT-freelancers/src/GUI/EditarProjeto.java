    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        //Variaveis e inputs
        Projeto proj = new Projeto();


        String nome = txtNome.getText();
        String nomeCliente = txtNomeCliente.getText();
        Float preco = Float.parseFloat(txtPreco.getText());
        int horas = Integer.parseInt(txtHoras.getText());

        if (nome.isEmpty() || nomeCliente.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos !");
            
        }

        Set<Utilizador> UtilizadorList = new HashSet<>();
        UtilizadorList.add(utilizador);
        Set<Tarefas> guardaTarefas = null;
        Set<Projeto> guarda = new HashSet<>();
        
        //Novo Projeto
        proj.setNome(nome);
        proj.setPrecoPhora(preco);
        proj.setTotalHoras(horas);
        proj.setNomeCliente(nomeCliente);
        proj.setUtil(UtilizadorList);
        
        
        for (Projeto projeto : Repositorio.getInstance().getProjeto_tarefas().keySet()) {
            if (projeto.getNome().equals(p.getNome())) {
                guardaTarefas = proj.getTarefaList();
                guarda.add(projeto);
            }
        }
        
        int i = JOptionPane.showConfirmDialog(null, "O Projeto ser√° alterado \nDeseja continuar?");
        if (i == JOptionPane.YES_OPTION) {
            for (Projeto projeto : guarda) {
                Repositorio.getInstance().getProjeto_tarefas().remove(projeto);
                Repositorio.getInstance().getProjeto_tarefas().put(proj, guardaTarefas);
            }
            JOptionPane.showMessageDialog(null, "Editado com Sucesso");
        } else if (i == JOptionPane.NO_OPTION) {
            System.out.println("Clicou em N√£o");
        } else if (i == JOptionPane.CANCEL_OPTION) {
            System.out.println("Clicou em Cancel");
        }


    }//GEN-LAST:event_jButton5ActionPerformed

