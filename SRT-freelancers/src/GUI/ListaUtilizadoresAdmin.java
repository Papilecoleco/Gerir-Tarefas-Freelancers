    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        //Variaveis
        DefaultTableModel model = (DefaultTableModel) tableUsers.getModel();
        int linha = tableUsers.getSelectedRow();
        String n = model.getValueAt(linha, 1).toString();
        
        //Remove um utilizador
        Iterator<Utilizador> iter = Repositorio.getInstance().getUtilizadores().iterator();
        while(iter.hasNext()){
            Utilizador u = iter.next();
            if(u.getEmail().equals(n)){
                iter.remove();
                RefreshTable();
                JOptionPane.showMessageDialog(null, "["+ n + "] removido com sucesso!");
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        //Variaveis
        DefaultTableModel model = (DefaultTableModel) tableUsers.getModel();
        int linha = tableUsers.getSelectedRow();
        String n = model.getValueAt(linha, 1).toString();
        
        //Atribui o tipo User Manager a um User
        Iterator<Utilizador> iter = Repositorio.getInstance().getUtilizadores().iterator();
        while(iter.hasNext()){
            Utilizador u = iter.next();
            if(u.getEmail().equals(n)){
                u.setTipo(TipoConta.USER);
                RefreshTable();
                JOptionPane.showMessageDialog(null,  "tipo de utilizador alterado!");
            }
        }
    }//GEN-LAST:event_jButton8ActionPerformed

